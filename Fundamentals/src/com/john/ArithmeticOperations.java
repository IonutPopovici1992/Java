package com.john;

/// Arithmetic Operations
public class ArithmeticOperations {

    public static void main(String[] args) {
        // Arithmetic Operations
        System.out.println(10 + 2);
        System.out.println(10 - 2);
        System.out.println(10 * 2);
        System.out.println(10 % 2);
        System.out.println(10 / 2);
        // BODMAS
        System.out.println(10 * (2 + 7) - 10);
    }

}
