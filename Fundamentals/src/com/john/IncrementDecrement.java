package com.john;

/// Increment and Decrement
public class IncrementDecrement {

    public static void main(String[] args) {
        // Increment and Decrement
        int number = 10;
        // number = number + 1;
        number++;
        System.out.println(number);

        // number = number - 1;
        number--;
        System.out.println(number);

        // number = number + 2;
        number += 2;
        System.out.println(number);

        // number = number - 2;
        number -= 2;
        System.out.println(number);
    }

}
